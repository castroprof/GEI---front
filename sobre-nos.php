      <p>
        O grupo que hoje chamamos de <strong>GEI</strong> (Grupo de Estudos Iyamopo) surgiu a partir de uma ideia há anos atrás: não era fácil (e acredito que ainda não seja) encontrar material confiável para pesquisa sobre temas ligados ao continente africano como: filosofia, arte, religião, cultura, etc..
      </p>
      <p>
        Em 2023, eu já pesquisava sobre arte e filosofia africana (principalmente iorubá) há pelo menos 10 anos, devido à necessidade na produção do Ateliê Iyamopo. Então convidei algumas pessoas para formar um grupo de pesquisa e ampliar assim os temas. Assim surgiu o Instituto Iyamopo e, agora em 2025, este grupo de pesquisa.
      </p>
